        <h2>What is Virtual Reality?</h2>
        <p>
            A VR makes a  person feel like he is within the scenario physically and, further, with the movement of head, the world also turns. 
            The display typically splits the image between the left and right eyes, creating a stereoscopic 3D effect with stereo sound and an immersive experience, allowing a person to visualise a virtual world being generated by the computer.  The definition comes from the terms ‘virtual’ and ‘reality’ viz. ‘virtual’ as ‘simulated’ by computer  and ‘reality’ as we experience as humans. 
            Therefore, the term VR can be also defined as ‘near-reality’. 
            In everyday life, we perceive  the world around us through our primary and secondary sensors. 
            This sensory information is processed by our brain to present a rich flow of information from the environment to our brains. 
            Hence, when our senses are fed with similar synthetic information, human perception of reality also changes in response to it. 
            VR presents a version of reality that isn’t really there, but from our perspective perceived as real.

        </p>
