    <h2> How is Virtual Reality achieved?</h2>

    <p>
        Today VR is implemented using computer technology, along with a range of systems such as headsets, special gloves etc. 
        These are used to actually stimulate our senses together in order to create the illusion of reality to give us an immersive experience. 
        Humans have nearly  180 degree vision and we are unconsciously aware of peripheral vision. 
        Whenever eyes and the vestibular system (sensory system in the ear that is responsible for providing our brain with information about motion, head position, and spatial orientation) are in conflict, it can cause motion sickness. 
        A good implementation of VR manages to get the combination of hardware, software and sensory synchronicity rightly balanced to achieve sense of balance i.e.  one really feels like they are present in that environment.
        </p>
